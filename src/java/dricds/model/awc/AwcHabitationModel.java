public class AwcHabitationModel {
    private int slno=0;
    private String habitationCode=null;
    private String awcCode=null;
    private String habitationName=null;
    private String habitationDescription=null;
    private String mapColour=null;
    private String mapLonglat=null;
    private String hidselpoints=null;

    public int getSlno() {
        return slno;
    }

    public void setSlno(int slno) {
        this.slno = slno;
    }

    public String getHabitationCode() {
        return habitationCode;
    }

    public void setHabitationCode(String habitationCode) {
        this.habitationCode = habitationCode;
    }

    public String getAwcCode() {
        return awcCode;
    }

    public void setAwcCode(String awcCode) {
        this.awcCode = awcCode;
    }

    public String getHabitationName() {
        return habitationName;
    }

    public void setHabitationName(String habitationName) {
        this.habitationName = habitationName;
    }

    public String getHabitationDescription() {
        return habitationDescription;
    }

    public void setHabitationDescription(String habitationDescription) {
        this.habitationDescription = habitationDescription;
    }

    public String getMapColour() {
        return mapColour;
    }

    public void setMapColour(String mapColour) {
        this.mapColour = mapColour;
    }

    public String getMapLonglat() {
        return mapLonglat;
    }

    public void setMapLonglat(String mapLonglat) {
        this.mapLonglat = mapLonglat;
    }

    public String getHidselpoints() {
        return hidselpoints;
    }

    public void setHidselpoints(String hidselpoints) {
        this.hidselpoints = hidselpoints;
    }
    
    
    
}
